package com.test.domain;

public class Output {

    private int verticesNum;
    private int edgesNum;
    private int connectedCompNum;
    private int minDegree;
    private int maxDegree;
    private int avgDegree;
    private int diameter;
    private int radius;

    public int getVerticesNum() {
        return verticesNum;
    }

    public void setVerticesNum(int verticesNum) {
        this.verticesNum = verticesNum;
    }

    public int getEdgesNum() {
        return edgesNum;
    }

    public void setEdgesNum(int edgesNum) {
        this.edgesNum = edgesNum;
    }

    public int getConnectedCompNum() {
        return connectedCompNum;
    }

    public void setConnectedCompNum(int connectedCompNum) {
        this.connectedCompNum = connectedCompNum;
    }

    public int getMinDegree() {
        return minDegree;
    }

    public void setMinDegree(int minDegree) {
        this.minDegree = minDegree;
    }

    public int getMaxDegree() {
        return maxDegree;
    }

    public void setMaxDegree(int maxDegree) {
        this.maxDegree = maxDegree;
    }

    public int getAvgDegree() {
        return avgDegree;
    }

    public void setAvgDegree(int avgDegree) {
        this.avgDegree = avgDegree;
    }

    public int getDiameter() {
        return diameter;
    }

    public void setDiameter(int diameter) {
        this.diameter = diameter;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
